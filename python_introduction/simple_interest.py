# P: Principal amount
principal = 1000

# R: Annual interest rate 
rate = 0.05

# T: Time 
time = 3

# Calculation --- Formula: Interest (I) = Principal (P) * Rate (R) * Time (T)
# The result is stored in 'interest'.
interest = principal * rate * time

# --- Output ---
print(f"The simple interest is: {interest}")